let inputNum = -1

if(inputNum > 0){
    console.log(inputNum + " is a positive number and not a zero")
} else if (inputNum < 0){
    console.log(inputNum + " is a negative number and not a zero")
} else if (inputNum === 0){
    console.log(inputNum + " is a zero")
} else{
    console.log(inputNum + " is not a number")
}